class Narrowcast
{
  public static void main(String[] args)
{
  float marks=79.5,sub=30.5;
  double dmarks=(double)marks;
  short submarks=(short)sub;
  System.out.println(marks+" is an interger datatype");
  System.out.println(dmarks+" is an double datatype");
  System.out.println(submarks+" is an short datatype");
}
}